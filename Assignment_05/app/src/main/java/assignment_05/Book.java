package assignment_05;

public class Book {

    private int id ; 
    private String title ; 
    private String author ; 
    private int price ; 
    private int quantity ; 

    public Book( 
        int id , 
        String title , 
        String author , 
        int price , 
        int quantity 
    ) {
        this.id = id ; 
        this.title = title ; 
        this.author = author ; 
        this.price = price ; 
        this.quantity = quantity ; 
    }
    
}
